import argparse

def main():
    parser = argparse.ArgumentParser(description="Rendering Engine Project Packager")
    parser.add_argument(
        "--packed",
        action="store_true",
        help="Pack all assets into Content/Pack.bin instead of copying loose files."
    )
    parser.add_argument(
        "--keep-plain",
        action="store_true",
        help="Keep loose asset files when --packed is used."
    )
    args = parser.parse_args()

    project_root = os.path.abspath(os.getcwd())
    project_name = os.path.basename(project_root)

    print("=============================================")
    print(f" Packaging project: {project_name}")
    print("=============================================")

    # Detect platform
    plat = detect_platform()
    print(f"[INFO] Platform: {plat}")

    build_root = find_build_root(project_root, project_name)
    if not build_root:
        print("[ERROR] Could not locate build directory for project.")
        print("Expected one of:")
        print("  ../../Build/TestApplications/<ProjectName>")
        print("  ../../Build/UserApplications/<ProjectName>")
        return

    build_release = os.path.join(build_root, "Release")
    build_debug = os.path.join(build_root, "Debug")

    if not os.path.isdir(build_release) and not os.path.isdir(build_debug):
        print("[ERROR] No built artifacts found.")
        print("Build your project first:")
        if plat == "Windows":
            print("    build_project.bat")
        else:
            print("    ./build_project.sh")
        return

    # Choose Release if possible
    binary_dir = build_release if os.path.isdir(build_release) else build_debug
    print(f"[INFO] Using binary directory: {binary_dir}")

    # Detect executable
    exe = None
    for f in os.listdir(binary_dir):
        full_path = os.path.join(binary_dir, f)
        if plat == "Windows" and f.lower().endswith(".exe"):
            exe = f
            break
        if plat in ("Linux", "FreeBSD"):
            # Executable file without extension or with any name
            if os.path.isfile(full_path) and os.access(full_path, os.X_OK):
                exe = f
                break

    if exe is None:
        print("[ERROR] Could not find executable in", binary_dir)
        return

    print(f"[INFO] Found executable: {exe}")

    # Version
    version = read_version(project_root)
    print(f"[INFO] Version: {version}")

    # Output package directory
    package_root = os.path.join(build_root, "Packages")
    ensure_dir(package_root)

    out_dir_name = f"{project_name}-v{version}-{plat}"
    out_dir = os.path.join(package_root, out_dir_name)
    if os.path.exists(out_dir):
        shutil.rmtree(out_dir)

    print(f"[INFO] Creating package folder: {out_dir}")
    ensure_dir(out_dir)

    # Folder structure
    bin_dst = ensure_dir(os.path.join(out_dir, "Binaries"))
    content_dst = ensure_dir(os.path.join(out_dir, "Content"))
    config_dst = ensure_dir(os.path.join(out_dir, "Config"))
    logs_dst = ensure_dir(os.path.join(out_dir, "Logs"))

    # Copy executable
    shutil.copy2(os.path.join(binary_dir, exe), bin_dst)
    print(f"[COPY] {exe} -> Binaries/{plat}")

    # Copy local shared libraries from binary dir
    if plat == "Windows":
        for f in os.listdir(binary_dir):
            if f.lower().endswith(".dll"):
                src = os.path.join(binary_dir, f)
                shutil.copy2(src, bin_dst)
                print(f"[COPY] {f} (DLL)")

        # Ensure Vulkan loader is present
        bundle_windows_dependencies(bin_dst)

    elif plat in ("Linux", "FreeBSD"):
        for f in os.listdir(binary_dir):
            if ".so" in f or f.endswith(".dylib"):
                src = os.path.join(binary_dir, f)
                shutil.copy2(src, bin_dst)
                print(f"[COPY] {f} (local shared lib)")

        # Collect additional non-system .so libs via ldd
        exe_path = os.path.join(binary_dir, exe)
        collect_unix_shared_libs(exe_path, bin_dst)

    # ----------------------------------------------------------------------------------
    # Resolve asset and config folders (cross-platform)
    # ----------------------------------------------------------------------------------

    # Primary locations (inside binary dir)
    content_primary = os.path.join(binary_dir, "Content")
    config_primary = os.path.join(binary_dir, "Config")

    # Fallback locations (inside project-level build root)
    content_fallback = os.path.join(build_root, "Content")
    config_fallback = os.path.join(build_root, "Config")

    # --- CONTENT HANDLING (PLAIN or PACKED) ---

    if args.packed:
        # Still need to copy content first (to source for packing)
        if os.path.isdir(content_primary):
            copy_tree(content_primary, content_dst)
            source_for_packing = content_primary
        elif os.path.isdir(content_fallback):
            copy_tree(content_fallback, content_dst)
            source_for_packing = content_fallback
        else:
            print("[ERROR] No Content folder found — cannot pack assets.")
            return

        # Now replace loose assets with Pack.bin / Pack.json
        pack_assets(source_for_packing, content_dst, keep_plain=args.keep_plain)

    else:
        # Normal mode: plain content
        if os.path.isdir(content_primary):
            copy_tree(content_primary, content_dst)
        elif os.path.isdir(content_fallback):
            copy_tree(content_fallback, content_dst)
        else:
            print("[WARN] No Content folder found (neither Release/ nor Build root).")# --- CONTENT HANDLING (PLAIN or PACKED) ---

    # --- Config ---
    if os.path.isdir(config_primary):
        copy_tree(config_primary, config_dst)
    elif os.path.isdir(config_fallback):
        copy_tree(config_fallback, config_dst)
    else:
        print("[WARN] No Config folder found.")

    # Logs (empty)
    ensure_dir(logs_dst)

    # Write manifest
    write_manifest(out_dir, project_name, version, plat)

    # Create ZIP archive
    zip_path = shutil.make_archive(out_dir, "zip", out_dir)
    print(f"[OK] Archive created: {zip_path}")

    print("\n=============================================")
    print(" Packaging COMPLETE!")
    print(f" Output folder:\n   {out_dir}")
    print("=============================================")


if __name__ == "__main__":
    main()
