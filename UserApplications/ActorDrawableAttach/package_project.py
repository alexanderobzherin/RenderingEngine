"""
@file package_project.py
@brief Packages a built Rendering Engine application into a distributable layout.

Usage:
    python3 package_project.py
    python3 package_project.py --packed
    python3 package_project.py --packed --keep-plain

Packaging modes:

  • Default (no arguments)
        Copies Content/ as loose files and folders.
        Produces the package:
            <Build>/<App>/Packages/<App>-vX.Y.Z-<Platform>/

  • --packed
        Packs all assets from Content/ into:
            Content/Pack.bin
            Content/Pack.json
        Removes loose folders unless --keep-plain is also provided.

  • --packed --keep-plain
        Generates Pack.bin/Pack.json but preserves original loose assets.
        Useful for debugging or verification.

Both Windows and Unix platforms are supported.
The resulting package folder is also archived as a ZIP.
"""

import os
import shutil
import sys
import re
import datetime
import subprocess
import json

# --------------------------------------------------------------------------------------
# Helpers
# --------------------------------------------------------------------------------------

def detect_platform():
    p = sys.platform
    if p.startswith("win"):
        return "Windows"
    elif p.startswith("linux"):
        return "Linux"
    elif p.startswith("freebsd"):
        return "FreeBSD"
    return "Unknown"


def copy_tree(src, dst):
    if not os.path.exists(src):
        print(f"[WARN] Missing folder: {src}")
        return
    if os.path.exists(dst):
        shutil.rmtree(dst)
    shutil.copytree(src, dst)
    print(f"[COPY] {src} -> {dst}")


def ensure_dir(path):
    os.makedirs(path, exist_ok=True)
    return path


def read_version(project_root):
    """
    Try to read version.h generated by engine OR fallback to 0.0.0
    """
    possible_paths = [
        os.path.join(project_root, "..", "..", "Build", "Intermediate", "Generated", "version.h"),
        os.path.join(project_root, "..", "..", "Build", "Installed", "RenderingEngine",
                     "RenderingLibrary", "Include", "version.h")
    ]

    for p in possible_paths:
        if os.path.isfile(p):
            with open(p, "r", encoding="utf-8") as f:
                txt = f.read()
            major = re.search(r"RENDERING_ENGINE_VERSION_MAJOR\s+(\d+)", txt)
            minor = re.search(r"RENDERING_ENGINE_VERSION_MINOR\s+(\d+)", txt)
            patch = re.search(r"RENDERING_ENGINE_VERSION_PATCH\s+(\d+)", txt)
            if major and minor and patch:
                return f"{major.group(1)}.{minor.group(1)}.{patch.group(1)}"

    return "0.0.0"


def write_manifest(dst_dir, project_name, version, platform_name):
    manifest = os.path.join(dst_dir, "Manifest.txt")
    with open(manifest, "w", encoding="utf-8") as f:
        f.write(f"Rendering Engine Application Package\n")
        f.write(f"=================================\n\n")
        f.write(f"Project : {project_name}\n")
        f.write(f"Version : {version}\n")
        f.write(f"Platform: {platform_name}\n")
        f.write(f"Date    : {datetime.datetime.now()}\n\n")
        f.write("Folder layout:\n")
        f.write("  Binaries/            : executable + required DLLs/.so\n")
        f.write("  Content/             : shaders, textures, models, fonts\n")
        f.write("  Config/              : config files\n")
        f.write("  Logs/                : runtime logs (empty)\n")
    print(f"[OK] Manifest written: {manifest}")

def find_build_root(project_root, project_name):
    """
    Handle both DevMode and SDK-user layout:

    DevMode:
      <repo_root>/TestApplications/<ProjectName>/
      <repo_root>/Build/TestApplications/<ProjectName>/

    SDK-user:
      <sdk_root>/ContentExamples/<ProjectName>/
      <sdk_root>/Build/UserApplications/<ProjectName>/
    """
    candidates = [
        os.path.join(project_root, "..", "..", "Build", "TestApplications", project_name),
        os.path.join(project_root, "..", "..", "Build", "UserApplications", project_name),
    ]

    for c in candidates:
        if os.path.isdir(c):
            return os.path.abspath(c)

    return None

# --------------------------------------------------------------------------------------
# Windows-specific dependency handling
# --------------------------------------------------------------------------------------


def find_vulkan_loader_windows():
    """
    Try to locate a redistributable vulkan-1.dll on Windows.
    Priority:
      1. VULKAN_SDK/Redistributable*/Bin*/vulkan-1.dll
      2. VULKAN_SDK/Bin*/vulkan-1.dll
      3. Any vulkan-1.dll found on PATH
    """
    vulkan_sdk = os.environ.get("VULKAN_SDK", None)
    candidates = []

    if vulkan_sdk:
        # Common redistributable layouts
        for sub in [
            "Redistributable\\Bin",
            "Redistributable\\Bin32",
            "Redistributable\\Bin64",
            "Bin",
            "Bin32",
            "Bin64",
        ]:
            candidates.append(os.path.join(vulkan_sdk, sub, "vulkan-1.dll"))

    # Also search PATH directories (last resort)
    for path_dir in os.environ.get("PATH", "").split(os.pathsep):
        if not path_dir:
            continue
        candidates.append(os.path.join(path_dir, "vulkan-1.dll"))

    for c in candidates:
        if os.path.isfile(c):
            return os.path.abspath(c)

    return None


def bundle_windows_dependencies(bin_dst):
    """
    Ensure that Vulkan loader is present in Binaries on Windows.
    """
    target_vulkan = os.path.join(bin_dst, "vulkan-1.dll")
    if os.path.isfile(target_vulkan):
        # Already present (e.g., copied from binary directory)
        print("[INFO] vulkan-1.dll already present in package.")
        return

    src = find_vulkan_loader_windows()
    if src:
        shutil.copy2(src, target_vulkan)
        print(f"[COPY] Vulkan loader: {src} -> {target_vulkan}")
    else:
        print("[WARN] Could not locate redistributable vulkan-1.dll. "
              "Target machine must have Vulkan runtime installed.")

# --------------------------------------------------------------------------------------
# Unix-specific dependency handling
# --------------------------------------------------------------------------------------


def is_system_lib_unix(path):
    """
    Heuristic filter for system libraries vs. 3rd-party libs on Unix.

    We treat libs under:
      /lib, /lib64, /usr/lib, /usr/lib64
    as system libraries (not copied).

    Anything under /usr/local, /opt, or engine/project-specific paths
    is treated as 3rd-party and copied.
    """
    if not path or not path.startswith("/"):
        return True  # non-absolute or weird: ignore

    real = os.path.realpath(path)
    system_prefixes = ("/lib", "/lib64", "/usr/lib", "/usr/lib64")

    return any(real.startswith(p) for p in system_prefixes)


def collect_unix_shared_libs(executable, dst_dir):
    """
    Run ldd on the executable and copy non-system shared libraries
    into dst_dir. This helps make the package more self-contained.
    """
    try:
        output = subprocess.check_output(["ldd", executable], text=True)
    except Exception as e:
        print(f"[WARN] Failed to run ldd on {executable}: {e}")
        return

    seen = set()

    for line in output.splitlines():
        line = line.strip()
        if not line:
            continue

        # Patterns:
        #   libX.so.1 => /path/to/libX.so.1 (0x...)
        #   /lib64/ld-linux-x86-64.so.2 (0x...)
        lib_path = None

        if "=>" in line:
            parts = line.split("=>", 1)
            right = parts[1].strip()
            if right == "not found":
                continue
            # right: "/path/to/lib.so (0xaddr)"
            if " " in right:
                candidate = right.split(" ", 1)[0]
            else:
                candidate = right
            if candidate.startswith("/"):
                lib_path = candidate
        else:
            # direct path form: "/lib64/ld-linux-x86-64.so.2 (0x...)"
            if line.startswith("/"):
                if " " in line:
                    candidate = line.split(" ", 1)[0]
                else:
                    candidate = line
                lib_path = candidate

        if not lib_path:
            continue

        lib_path = os.path.realpath(lib_path)

        # Filter out system libs
        if is_system_lib_unix(lib_path):
            continue

        if lib_path in seen:
            continue

        seen.add(lib_path)

        if not os.path.isfile(lib_path):
            continue

        dest = os.path.join(dst_dir, os.path.basename(lib_path))
        if not os.path.isfile(dest):
            shutil.copy2(lib_path, dest)
            print(f"[COPY] Unix shared lib: {lib_path} -> {dest}")


